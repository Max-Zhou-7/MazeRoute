from lab04 import *
def test_straight_forward_maze():
    maze1 = [
['+','+','+','+','+','+'],
['+','G',' ',' ',' ','+'],
['+','+','+','+','+','+'] ]
    assert solveMaze(maze1, 1, 4) == True
    assert maze1 == [
['+','+','+','+','+','+'],
['+','G',3,2,1,'+'],
['+','+','+','+','+','+'] ]

def test_no_solution():
    maze2 = [
        ['+','+','+'],
        ['+',' ','+'],
        ['+','+','+']
    ]
    assert solveMaze(maze2,1,1) == False
    assert maze2 == [
        ['+','+','+'],
        ['+',1,'+'],
        ['+','+','+']
    ]
    maze3 = [
        ['+','+','+','+'],
        ['+',' ',' ','+'],
        ['+',' ',' ','+'],
        ['+','+','+','+']
    ]
    assert solveMaze(maze3,2,1) == False
    assert maze3 == [
        ['+','+','+','+'],
        ['+',2,3,'+'],
        ['+',1,4,'+'],
        ['+','+','+','+']
    ]

def test_openMaze():
    maze = [ ['+','+','+','+','+','+','+','+'],
             ['+',' ',' ',' ',' ',' ','G','+'],
             ['+',' ',' ',' ',' ',' ',' ','+'],
             ['+',' ',' ',' ',' ',' ',' ','+'],
             ['+',' ',' ',' ',' ',' ',' ','+'],
             ['+',' ',' ',' ',' ',' ',' ','+'],
             ['+','+','+','+','+','+','+','+'] ]
    assert solveMaze(maze, 5, 5) == True
    assert maze == [['+', '+', '+', '+', '+', '+', '+', '+'],
                    ['+', 9, 8, 7, 6, 5, 'G', '+'],
                    ['+', 10, 17, 18, 25, 4, ' ', '+'],
                    ['+', 11, 16, 19, 24, 3, ' ', '+'],
                    ['+', 12, 15, 20, 23, 2, ' ', '+'],
                    ['+', 13, 14, 21, 22, 1, ' ', '+'],
                    ['+', '+', '+', '+', '+', '+', '+', '+']]
    maze3 = [ ['+','+','+','+','+'],
         ['+',' ',' ','G','+'],
         ['+',' ',' ',' ','+'],
         ['+',' ', 1 ,' ','+'],
         ['+','+','+','+','+'] ]
    assert solveMaze(maze3,3,2) == True
    assert maze3 == [ ['+','+','+','+','+'],
         ['+', 4 , 3 ,'G','+'],
         ['+', 5 , 2 ,' ','+'],
         ['+', 6 , 1 ,' ','+'],
         ['+','+','+','+','+'] ]

